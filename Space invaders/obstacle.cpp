Obstacle::Obstacle(const Vector2& position)
	:m_position(position) {

    for (size_t row = 0; row < m_grid.size(); ++row) {
        for (size_t column = 0; column < m_grid.at(0).size(); ++column) {
            if (m_grid.at(row).at(column) == 1) {
                float pos_x = position.x + column * 3;
                float pos_y = position.y + row * 3;
                Block block = Block({ pos_x, pos_y });
                m_blocks.push_back(block);
            }
        }
    }
}

void Obstacle::draw() {
    for (auto& block : m_blocks) {
        block.draw();
    }
}